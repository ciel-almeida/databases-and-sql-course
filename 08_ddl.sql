MODIFY VALOR_UNITARIO VARCHAR(50) NOT NULL;

/* Adding a new column */
ALTER TABLE PRODUTO
ADD PESO FLOAT(10, 2) NOT NULL;

/* Adding a new column at a specific order in the table - MySQL only */
ALTER TABLE PRODUTO
ADD PESO FLOAT(10,2) NOT NULL
AFTER NOME_PRODUTO;

